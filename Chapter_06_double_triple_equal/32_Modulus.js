//modulus operator '%' sign is nothing but modulus operator which gives the remainder of the division of two numbers

let a = 10, b = 3;

let mod = a % b; //modulus operator (%)
console.log(mod); //1

let result = 13 % 7;
console.log(result); //6

let result1 = 20 % 6;
console.log(result1); //2       

console.log(101 % 2); //1   
console.log(100 % 2); //0 
console.log(37 % 2); //1    
console.log(38 % 2); //0
// n%1 = odd number => gives 1
// n%2 = even number => gives 0 
