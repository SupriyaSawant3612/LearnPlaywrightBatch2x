let x = 10;
x += 10; //x = x + 10
console.log(x); //20

let y = 20;
y -= 5; //y = y - 5
console.log(y); //15 
//compound assignment operator (+=, -=, *=, /=, %=, **=) which is used to perform an operation and assign the result to the variable in one step.
x *= 2; //x = x * 2
console.log(x); //40

y /= 5; //y = y / 5
console.log(y); //3

let x = 10;
x -= -10;
console.log(x); //20

x %= 2;
console.log(x); //0
